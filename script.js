// START OF ANIMATED TEXT FUNCTION
// retrieved from https://webdevtrick.com/javascript-typing-effect/
function animatedText() {
    const typedTextSpan = document.querySelector(".emphasisHeader");
    const textArray = ["Front-End Developer", "GIS Enthusiast", "Canoe Tripper"];
    const typingDelay = 200;
    const erasingDelay = 100;
    const newTextDelay = 100;
    let textArrayIndex = 0;
    let charIndex = 0;

    function type() {
        if (charIndex < textArray[textArrayIndex].length) {
            typedTextSpan.textContent += textArray[textArrayIndex].charAt(charIndex);
            charIndex++;
            setTimeout(type, typingDelay);
        } else {
            setTimeout(erase, newTextDelay);
        }
    }

    function erase() {
        if (charIndex > 0) {
            typedTextSpan.textContent = textArray[textArrayIndex].substring(0, charIndex - 1);
            charIndex--;
            setTimeout(erase, erasingDelay);
        } else {
            textArrayIndex++;
            if (textArrayIndex >= textArray.length) textArrayIndex = 0;
            setTimeout(type, typingDelay + 400);
        }
    }

    setTimeout(type, newTextDelay);
}

animatedText();

// declaration of array to hold routes
let routesToDel = [];

// simple function to add marker to map and click listener to panto/flyto location
function addMarkersPlusContent(props) {
    const marker = new mapboxgl.Marker()
        .setLngLat(props.coords)
        .setPopup(new mapboxgl.Popup().setHTML(props.content))
        .addTo(map);

    if (props.name != "toronto") {
        marker.getElement().addEventListener("click", () => {
            // iteration through an array holding each route after it has been added to the map
            for (let i = 0; i < routesToDel.length; i++) {
                map.removeLayer(routesToDel[i]).removeSource(routesToDel[i]);
                routesToDel.splice(i);
            }

            // pan to and zoom into river
            map.flyTo({ center: props.coords, zoom: props.zoom });

            // if the route is not currently displayed, add route to map
            if (!map.getLayer(`${props.name}`)) {
                routesToDel.push(`${props.name}`);
                map.addSource(`${props.name}`, {
                    type: "geojson",
                    data: {
                        type: "Feature",
                        properties: {},
                        geometry: {
                            type: "LineString",
                            coordinates: props.routeCoords,
                        },
                    },
                });
                map.addLayer({
                    id: `${props.name}`,
                    type: "line",
                    source: `${props.name}`,
                    layout: {
                        "line-join": "round",
                        "line-cap": "round",
                    },
                    paint: {
                        "line-color": "lightblue",
                        "line-width": 5,
                    },
                });
            }
            // end of if checking if route is displayed
        });
    }
}

// START OF CODE TO CHECK IF MAP IS IN WINDOW VIEW, IF IN VIEW, LOAD MAP POPUP INTRODUCING WHAT MAP IS SHOWING
// when you load a popup on mapbox it scrolls the entire window to view the map, not something you want happening when the map is at the bottom of your page.
// this code was adapted for my site and found from this codepen: https://codepen.io/jr-cologne/pen/zdYdmx

// get the map to add popup
let element = document.getElementById("map");
let elementHeight = element.clientHeight;

// listen for scroll event and call animate function
document.addEventListener("scroll", addIntroPopup);

// check if element is in view
function inView() {
    // get window height
    let windowHeight = window.innerHeight;
    // get number of pixels that the document is scrolled
    let scrollY = window.scrollY || window.pageYOffset;
    // get current scroll position (distance from the top of the page to the bottom of the current viewport)
    let scrollPosition = scrollY + windowHeight;
    // get element position (distance from the top of the page to the bottom of the element)
    let elementPosition = element.getBoundingClientRect().top + scrollY + elementHeight;

    // is scroll position greater than element position? (is element in view?)
    if (scrollPosition > elementPosition) {
        return true;
    }
    return false;
}

// add popup when map element is in view
function addIntroPopup() {
    // is element in view?
    if (inView()) {
        // element is in view, add mapbox popup
        new mapboxgl.Popup()
            .setLngLat([-90, 53.7])
            .setHTML(
                "<p><strong>Here are some of my favourite canoe trips I've led.</strong></p> <p>Select a marker to see a trip photo!</p><p>If you have any questions, shoot me a message - I could talk forever about them.</p>"
            )
            .addTo(map);

        // removing event listener so user only views popup message once
        document.removeEventListener("scroll", addIntroPopup);
    }
}

